async def main():
    import argparse
    from dotenv import load_dotenv

    load_dotenv()

    parser = argparse.ArgumentParser(description="Generate answers from baselines")
    parser.add_argument("--baselines", type=str, default="B1,B2,B3,B4")
    parser.add_argument("--questions", type=int, default=5, help="Max questions to process")
    parser.add_argument("--top-k", type=int, default=10)
    args = parser.parse_args()

    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(message)s")

    # Load questions
    with open(EVAL_DIR / "gold_standard" / "questions.json") as f:
        data = json.load(f)
    questions = data.get("questions", data)

    # Connect Neo4j
    from graph.neo4j_client import Neo4jClient, Neo4jConfig

    config = Neo4jConfig.from_env()
    neo4j = Neo4jClient(config)
    await neo4j.__aenter__()

    try:
        baseline_names = args.baselines.upper().split(",")
        results = await run_all_baselines(
            neo4j, questions, baseline_names, args.top_k, args.questions
        )
        save_answers(results)

        # Print summary
        for b, answers in results.items():
            avg_papers = sum(a.num_papers_retrieved for a in answers) / max(len(answers), 1)
            avg_time = sum(a.generation_time_ms for a in answers) / max(len(answers), 1)
            print(f"{b}: {len(answers)} answers, avg {avg_papers:.1f} papers, avg {avg_time:.0f}ms")
    finally:
        await neo4j.__aexit__(None, None, None)


if __name__ == "__main__":
    asyncio.run(main())
