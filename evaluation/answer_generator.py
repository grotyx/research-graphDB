_openai_client = None


async def _get_embedding(text: str) -> list[float]:
    """Get OpenAI embedding for text."""
    global _openai_client
    from openai import OpenAI

    if _openai_client is None:
        _openai_client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

    response = _openai_client.embeddings.create(
        model="text-embedding-3-large",
        input=text[:8000],
        dimensions=3072,
    )
    return response.data[0].embedding


async def _generate_hyde(question: str) -> Optional[str]:
    """Generate hypothetical answer for HyDE (Hypothetical Document Embedding)."""
    import anthropic

    try:
        result = await _call_llm(
            system_prompt=(
                "You are a spine surgery expert. Write a short paragraph (3-5 sentences) "
                "answering the following question based on your knowledge. "
                "Include specific study types, outcomes, and findings."
            ),
            user_prompt=question,
            max_tokens=1000,
        )
        return result if not result.startswith("Error:") else None
    except Exception as e:
        logger.warning("HyDE generation failed: %s", e)
        return None


def _detect_query_type(question: str) -> str:
    """Detect query type from question text for dynamic weight selection."""
    q = question.lower()
    comparison_keywords = ["versus", " vs ", "compared", "comparing", "comparison", "vs."]
    evidence_keywords = ["evidence for", "evidence of", "rct", "meta-analysis", "systematic review"]
    mechanism_keywords = ["risk factor", "mechanism", "pathogenesis", "why does", "how does"]

    if any(kw in q for kw in comparison_keywords):
        return "comparison"
    elif any(kw in q for kw in evidence_keywords):
        return "evidence"
    elif any(kw in q for kw in mechanism_keywords):
        return "mechanism"
    return "default"


async def _generate_with_llm(question: str, context: str) -> str:
    """Generate answer using Claude Haiku with retrieved context."""
    import anthropic

    return await _call_llm(
        system_prompt=(
            "You are a spine surgery evidence synthesis system. Answer the clinical "
            "question based ONLY on the provided papers. For each claim:\n"
            "1. Cite the paper ID in brackets [paper_id]\n"
            "2. Mention the study design and evidence level\n"
            "3. Include specific numbers when available\n"
            "4. Organize by outcome domain\n"
            "5. Acknowledge gaps in the evidence\n"
            "Do NOT use any knowledge outside the provided papers."
        ),
        user_prompt=f"## Retrieved Papers\n\n{context}\n\n## Clinical Question\n\n{question}",
        max_tokens=4000,
    )


def _build_context(results) -> str:
    """Build context string from BaselineResult list."""
    parts = []
    for r in results:
        parts.append(
            f"[{r.paper_id}] {r.title} (Evidence: {r.evidence_level or 'unknown'}, Year: {r.year or 'N/A'})"
        )
    return "\n\n".join(parts)


