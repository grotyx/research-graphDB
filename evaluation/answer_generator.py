_openai_client = None


async def _get_embedding(text: str) -> list[float]:
    """Get OpenAI embedding for text."""
    global _openai_client
    from openai import OpenAI

    if _openai_client is None:
        _openai_client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

    response = _openai_client.embeddings.create(
        model="text-embedding-3-large",
        input=text[:8000],
        dimensions=3072,
    )
    return response.data[0].embedding


async def _get_graph_traversal_summary(neo4j_client: Any, question: str) -> str:
    """Get graph traversal summary for questions that benefit from it.

    Detects question type and runs appropriate graph traversal:
    - "all interventions" / "compare" → list interventions + outcomes from graph
    - "evidence chain" → traverse intervention→pathology→outcome chain

    Returns short summary for system prompt (not context — v6 lesson).
    Returns empty string if not applicable.
    """
    q = question.lower()

    try:
        from solver.graph_traversal_search import GraphTraversalSearch
        gts = GraphTraversalSearch(neo4j_client)

        # Type 1: "all interventions for pathology" → list from graph
        if any(kw in q for kw in ["all", "available", "every", "모든", "전체", "가능한"]):
            # Extract pathology from question
            entities = await _extract_entities_from_question(question)
            pathologies = entities.get("pathologies", [])
            if pathologies:
                path_name = pathologies[0]
                query = """
                MATCH (i:Intervention)-[:TREATS]->(p:Pathology)
                WHERE toLower(p.name) CONTAINS toLower($name)
                RETURN DISTINCT i.name AS intervention
                ORDER BY i.name
                LIMIT 15
                """
                rows = await neo4j_client.run_query(query, {"name": path_name})
                if rows:
                    interventions = [r["intervention"] for r in rows if r.get("intervention")]
                    if interventions:
                        return f"Interventions treating {path_name} in the database: {', '.join(interventions)}"

        # Type 2: "evidence chain" / "trace" → traverse
        if any(kw in q for kw in ["evidence chain", "chain", "trace", "근거 체인", "근거 사슬"]):
            entities = await _extract_entities_from_question(question)
            interventions = entities.get("interventions", [])
            pathologies = entities.get("pathologies", [])
            if interventions and pathologies:
                result = await gts.traverse_evidence_chain(
                    intervention=interventions[0],
                    pathology=pathologies[0],
                )
                if result.outcomes:
                    outcomes_str = ", ".join(o.get("name", str(o)) if isinstance(o, dict) else str(o) for o in result.outcomes[:8])
                    direct_count = len(result.direct_evidence)
                    related_count = len(result.related_evidence)
                    return (
                        f"Evidence chain: {interventions[0]} → TREATS → {pathologies[0]} → AFFECTS → {outcomes_str}\n"
                        f"Direct evidence: {direct_count} papers, Related (IS_A siblings): {related_count} papers"
                    )

        # Type 3: "shared/unique outcomes" → structured comparison (GR 질문 전용)
        # 일반 "compared to" 비교 질문은 제외 — graph traversal 불필요
        if any(kw in q for kw in ["shared", "unique to each", "공통", "고유한"]):
            entities = await _extract_entities_from_question(question)
            interventions = entities.get("interventions", [])
            pathologies = entities.get("pathologies", [])
            if len(interventions) >= 2 and pathologies:
                result = await gts.compare_interventions(
                    int1=interventions[0],
                    int2=interventions[1],
                    pathology=pathologies[0],
                )
                shared = len(result.shared_outcomes)
                unique1 = len(result.int1_only_outcomes)
                unique2 = len(result.int2_only_outcomes)
                if shared + unique1 + unique2 > 0:
                    return (
                        f"Graph comparison: {interventions[0]} vs {interventions[1]} for {pathologies[0]}\n"
                        f"Shared outcomes: {shared}, {interventions[0]}-only: {unique1}, {interventions[1]}-only: {unique2}"
                    )

    except Exception as e:
        logger.warning("Graph traversal summary failed: %s", e)

    return ""


async def _isa_expand_search(
    neo4j_client: Any, existing_pids: set[str], top_k: int, question: str = ""
) -> list[dict]:
    """IS_A expansion v3 — depth=1 + pathology check + keyword filter.

    검색된 논문의 IS_A sibling으로 추가 논문 보충하되:
    1. depth=1만 (직접 parent의 sibling만, grandparent 금지)
    2. Fix 2: sibling 논문이 원래 논문과 같은 pathology 계열인지 확인
    3. 추가 논문 제목이 질문 키워드를 포함해야 함 (무관 논문 차단)
    """
    if not existing_pids:
        return []

    pid_list = list(existing_pids)[:20]

    # 질문에서 키워드 추출 (3글자 이상, stop words 제외)
    stop_words = {
        "what", "is", "the", "are", "for", "in", "of", "and", "or", "a", "an",
        "to", "from", "with", "by", "on", "at", "how", "does", "do", "which",
        "that", "this", "than", "between", "after", "before", "current",
        "evidence", "best", "role", "impact", "effect", "based", "compared",
        "comparing", "outcomes", "results", "clinical", "surgical", "treatment",
    }
    keywords = [
        w.lower() for w in re.findall(r"[A-Za-z0-9-]+", question)
        if w.lower() not in stop_words and len(w) >= 3
    ]

    # Fix 2: pathology-aware IS_A expansion
    # 원래 논문의 pathology를 수집한 후, sibling 논문도 같은 pathology를 다루는지 확인
    query = """
    UNWIND $pids AS pid
    MATCH (p:Paper {paper_id: pid})-[:INVESTIGATES]->(i:Intervention)-[:IS_A]->(parent:Intervention)
    MATCH (sibling:Intervention)-[:IS_A]->(parent)
    WHERE sibling <> i
    MATCH (sibling)<-[:INVESTIGATES]-(p2:Paper)
    WHERE NOT p2.paper_id IN $pids
    // Fix 2: pathology 일치 확인 — 원래 논문과 sibling 논문이 같은 pathology 계열
    MATCH (p)-[:STUDIES]->(origPath:Pathology)
    WHERE (p2)-[:STUDIES]->(origPath)
       OR EXISTS {
           MATCH (p2)-[:STUDIES]->(sibPath:Pathology)-[:IS_A*..2]->(origPath)
       }
       OR EXISTS {
           MATCH (p2)-[:STUDIES]->(sibPath:Pathology)<-[:IS_A*..2]-(origPath)
       }
    RETURN DISTINCT p2.paper_id AS paper_id, p2.title AS title,
           p2.evidence_level AS evidence_level, p2.year AS year,
           0.6 AS score
    LIMIT $limit
    """
    try:
        rows = await neo4j_client.run_query(query, {
            "pids": pid_list, "limit": top_k * 3
        })
        results = []
        for r in rows:
            pid = r.get("paper_id", "")
            title = r.get("title", "")
            if not pid or pid in existing_pids:
                continue

            # Keyword filter: 제목에 질문 키워드가 최소 2개 포함되어야 함
            title_lower = title.lower()
            match_count = sum(1 for kw in keywords if kw in title_lower)
            min_matches = 2 if len(keywords) >= 3 else 1
            if match_count < min_matches:
                continue

            chunk_rows = await neo4j_client.run_query(
                "MATCH (p:Paper {paper_id: $pid})-[:HAS_CHUNK]->(c:Chunk) "
                "WHERE c.tier = 'tier1' RETURN c.content AS content LIMIT 1",
                {"pid": pid}
            )
            content = chunk_rows[0]["content"][:500] if chunk_rows else ""
            results.append({
                "paper_id": pid,
                "title": title,
                "evidence_level": r.get("evidence_level", "unknown"),
                "content": content,
                "score": 0.6,
            })
        return results
    except Exception as e:
        logger.warning("IS_A expand query failed: %s", e)
        return []


