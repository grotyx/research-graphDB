_openai_client = None


async def _get_embedding(text: str) -> list[float]:
    """Get OpenAI embedding for text."""
    global _openai_client
    from openai import OpenAI

    if _openai_client is None:
        _openai_client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

    response = _openai_client.embeddings.create(
        model="text-embedding-3-large",
        input=text[:8000],
        dimensions=3072,
    )
    return response.data[0].embedding


async def _generate_with_llm(question: str, context: str) -> str:
    """Generate answer using Claude Haiku with retrieved context."""
    import anthropic

    client = anthropic.Anthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))
    response = client.messages.create(
        model="claude-haiku-4-5-20251001",
        max_tokens=2000,
        system=(
            "You are a spine surgery evidence synthesis system. Answer the clinical "
            "question based ONLY on the provided papers. For each claim:\n"
            "1. Cite the paper ID in brackets [paper_id]\n"
            "2. Mention the study design and evidence level\n"
            "3. Include specific numbers when available\n"
            "4. Organize by outcome domain\n"
            "5. Acknowledge gaps in the evidence\n"
            "Do NOT use any knowledge outside the provided papers."
        ),
        messages=[
            {
                "role": "user",
                "content": f"## Retrieved Papers\n\n{context}\n\n## Clinical Question\n\n{question}",
            }
        ],
    )
    return response.content[0].text


def _build_context(results) -> str:
    """Build context string from BaselineResult list."""
    parts = []
    for r in results:
        parts.append(
            f"[{r.paper_id}] {r.title} (Evidence: {r.evidence_level or 'unknown'}, Year: {r.year or 'N/A'})"
        )
    return "\n\n".join(parts)


