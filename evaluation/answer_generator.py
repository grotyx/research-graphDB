# ============================================================================
# LLM Configuration — set via .env
# ============================================================================
# EVAL_LLM_PROVIDER: "openai" or "anthropic"
# EVAL_LLM_MODEL: model name (e.g., "gpt-5.4-mini", "claude-haiku-4-5-20251001")
# Falls back to anthropic/claude-haiku if not set

_llm_provider: Optional[str] = None
_llm_model: Optional[str] = None
_llm_client: Any = None


def _get_llm_config() -> tuple[str, str]:
    """Get LLM provider and model from env."""
    global _llm_provider, _llm_model
    if _llm_provider is None:
        _llm_provider = os.getenv("EVAL_LLM_PROVIDER", "anthropic").lower()
        _llm_model = os.getenv("EVAL_LLM_MODEL", "claude-haiku-4-5-20251001")
        logger.info("Eval LLM: provider=%s, model=%s", _llm_provider, _llm_model)
    return _llm_provider, _llm_model


def _get_llm_client() -> Any:
    """Get or create LLM client."""
    global _llm_client
    if _llm_client is not None:
        return _llm_client

    provider, model = _get_llm_config()
    if provider == "openai":
        from openai import OpenAI
        _llm_client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))
    else:
        import anthropic
        _llm_client = anthropic.Anthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))
    return _llm_client


async def _call_llm(
    system_prompt: str, user_prompt: str, max_tokens: int = 4000,
    model_override: str = None,
) -> str:
    """Unified LLM call — works with both OpenAI and Anthropic."""
    provider, model = _get_llm_config()
    if model_override:
        # v19: Sonnet override — force Anthropic provider
        model = model_override
        provider = "anthropic"
        logger.info("Model override: %s", model)
    client = _get_llm_client()

    try:
        if provider == "openai":
            response = client.chat.completions.create(
                model=model,
                max_tokens=max_tokens,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_prompt},
                ],
            )
            return response.choices[0].message.content
        else:
            response = client.messages.create(
                model=model,
                max_tokens=max_tokens,
                system=system_prompt,
                messages=[{"role": "user", "content": user_prompt}],
            )
            return response.content[0].text
    except Exception as e:
        logger.error("LLM call failed: %s", e)
        return f"Error: {e}"


@dataclass
class GeneratedAnswer:
    """Answer generated by a baseline system."""
    query_id: str
    question: str
    baseline: str
    answer: str
    cited_papers: list[dict] = field(default_factory=list)
    num_papers_retrieved: int = 0
    evidence_levels: list[str] = field(default_factory=list)
    generation_time_ms: int = 0


async def generate_answer_b1_keyword(
    neo4j_client: Any, question: str, top_k: int = 10
) -> tuple[str, list[dict]]:
    """B1: Keyword search → answer generation."""
    from evaluation.baselines import KeywordSearch

    searcher = KeywordSearch(neo4j_client)
    results = await searcher.search(question, top_k=top_k)

    if not results:
        return "No relevant papers found for this query.", []

    context = _build_context(results)
    answer = await _generate_with_llm(question, context)
    papers = [{"paper_id": r.paper_id, "title": r.title, "evidence_level": r.evidence_level, "year": r.year} for r in results]
    return answer, papers


async def generate_answer_b2_vector(
    neo4j_client: Any, question: str, top_k: int = 10
) -> tuple[str, list[dict]]:
    """B2: Vector-only search → answer generation."""
    embedding = await _get_embedding(question)

    rows = await neo4j_client.vector_search_chunks(
        embedding=embedding, top_k=top_k * 3, min_score=0.3
    )

    # Dedup by paper
    seen = {}
    for r in rows:
        pid = r.get("paper_id", "")
        if pid and pid not in seen:
            seen[pid] = r

    papers_list = list(seen.values())[:top_k]
    if not papers_list:
        return "No relevant papers found for this query.", []

    # Build context from chunks
    context_parts = []
    papers = []
    for r in papers_list:
        title = r.get("paper_title", "Unknown")
        el = r.get("evidence_level", "unknown")
        content = r.get("content", "")[:500]
        pid = r.get("paper_id", "")
        context_parts.append(f"[{pid}] {title} (Evidence: {el})\n{content}")
        papers.append({"paper_id": pid, "title": title, "evidence_level": el})

    context = "\n\n".join(context_parts)
    answer = await _generate_with_llm(question, context)
    return answer, papers


async def generate_answer_b3_llm_direct(question: str) -> tuple[str, list[dict]]:
    """B3: LLM direct — no knowledge graph."""
    answer = await _call_llm(
        system_prompt=(
            "You are a spine surgery expert. Answer the clinical question based on "
            "your medical knowledge. For each point, cite specific studies by first "
            "author and year. Be specific about study designs and evidence levels. "
            "Organize your response by outcome domain."
        ),
        user_prompt=question,
        max_tokens=4000,
    )
    return answer, []  # No verifiable papers


_b4_version: str = "v14"  # default: v14 base

async def generate_answer_b4_graphrag(
    neo4j_client: Any, question: str, top_k: int = 10
) -> tuple[str, list[dict]]:
    """B4: GraphRAG v7 — Hybrid search + LLM Reranker + Graph-aware prompt.

    v1-v2 복원 (최고 성적) + Graph hint in system prompt.
    Graph 정보를 context에 넣지 않고, system prompt에 1줄 힌트만 제공.

    Pipeline:
        Step 1: HyDE → better embedding
        Step 2: TieredHybridSearch (graph+vector 합산 ranking)
        Step 3: LLM Reranker (질문 적합성 재정렬)
        Step 4: Graph hint 생성 (1줄 요약)
        Step 5: 답변 생성 (B2와 동일 형식 + Graph hint)
    """
    from solver.tiered_search import TieredHybridSearch, SearchInput, SearchTier

    search = TieredHybridSearch(
        neo4j_client=neo4j_client,
        config={
            "use_neo4j_hybrid": True,
            "reranker_provider": "llm",
            "reranker_model": os.getenv("EVAL_LLM_MODEL", "claude-haiku-4-5-20251001"),
        },
    )

    # v14: Paper diversity — request more chunks to find more unique papers
    search_input = SearchInput(
        query=question,
        top_k=top_k * 5,  # v14: 3→5배로 증가, paper diversity 확보
        tier_strategy=SearchTier.TIER1_THEN_TIER2,
        use_hyde=True,
        use_reranker=True,
    )

    output = await search.search(search_input)

    # Dedup by paper — 논문당 best chunk 1개만 (diversity 핵심)
    seen: dict[str, dict] = {}
    for r in output.results:
        pid = r.chunk.document_id or ""
        if not pid:
            continue
        score = r.score
        if pid not in seen or score > seen[pid].get("score", 0):
            seen[pid] = {
                "paper_id": pid,
                "title": r.chunk.title or "",
                "evidence_level": str(r.chunk.evidence_level.value if hasattr(r.chunk.evidence_level, 'value') else r.chunk.evidence_level) if r.chunk.evidence_level else "unknown",
                "content": r.chunk.text[:500] if r.chunk.text else "",
                "score": score,
            }
    hybrid_paper_count = len(seen)

    # Multi-vector search — paper-level 검색으로 추가 diversity 확보
    try:
        embedding = await _get_embedding(question)
        mv_rows = await neo4j_client.search.multi_vector_search(
            embedding=embedding, top_k=top_k * 3, min_score=0.3
        )
        for r in mv_rows:
            pid = r.get("paper_id", "")
            if pid and pid not in seen:
                seen[pid] = {
                    "paper_id": pid,
                    "title": r.get("paper_title", "Unknown"),
                    "evidence_level": r.get("evidence_level", "unknown"),
                    "content": r.get("content", "")[:500],
                    "score": r.get("score", 0.0) * 0.9,
                }
    except Exception as e:
        logger.warning("Multi-vector search failed (non-critical): %s", e)

    # v16: 직접 검색 결과에 1-keyword filter (v14에서는 skip)
    if _b4_version in ("v16", "v20"):
        stop_words = {
            "what", "is", "the", "are", "for", "in", "of", "and", "or", "a", "an",
            "to", "from", "with", "by", "on", "at", "how", "does", "do", "which",
            "that", "this", "than", "between", "after", "before", "current",
            "evidence", "best", "role", "impact", "effect", "based", "compared",
            "comparing", "outcomes", "results", "clinical", "surgical", "treatment",
        }
        kw_list = [
            w.lower() for w in re.findall(r"[A-Za-z0-9-]+", question)
            if w.lower() not in stop_words and len(w) >= 3
        ]
        before_filter = len(seen)
        for pid in list(seen.keys()):
            title_lower = seen[pid].get("title", "").lower()
            if not any(kw in title_lower for kw in kw_list):
                del seen[pid]
        if len(seen) < before_filter:
            logger.info("v16 direct keyword filter: %d → %d papers (%d removed)",
                         before_filter, len(seen), before_filter - len(seen))

    logger.info("Direct search: %d unique papers", len(seen))

    # v13: IS_A expansion + pathology check + re-scoring
    # Fix 2: pathology 일치 확인, Fix 3: 직접 검색 최하위보다 나을 때만 교체
    isa_additions = []
    try:
        found_pids = set(seen.keys())
        isa_additions = await _isa_expand_search(neo4j_client, found_pids, top_k, question)
        if isa_additions:
            logger.info("IS_A expansion candidates: %d (pathology+keyword filtered)", len(isa_additions))
    except Exception as e:
        logger.warning("IS_A expansion failed (non-critical): %s", e)

    # Fix 3: 직접 검색 결과 먼저 확정, IS_A는 최하위 교체만
    direct_papers = sorted(seen.values(), key=lambda x: x.get("score", 0), reverse=True)[:top_k]
    if isa_additions and len(direct_papers) >= top_k:
        min_direct_score = direct_papers[-1].get("score", 0)
        replaced = 0
        for isa_p in sorted(isa_additions, key=lambda x: x.get("score", 0), reverse=True):
            pid = isa_p.get("paper_id", "")
            if pid in seen:
                continue
            if isa_p.get("score", 0) > min_direct_score:
                direct_papers[-1] = isa_p
                seen[pid] = isa_p
                direct_papers.sort(key=lambda x: x.get("score", 0), reverse=True)
                min_direct_score = direct_papers[-1].get("score", 0)
                replaced += 1
            else:
                break  # 이후 IS_A 논문은 더 낮으므로 중단
        if replaced:
            logger.info("IS_A re-scoring: replaced %d lowest direct papers", replaced)
    elif isa_additions:
        # 직접 검색이 top_k 미만이면 IS_A로 보충
        for isa_p in isa_additions:
            pid = isa_p.get("paper_id", "")
            if pid not in seen and len(direct_papers) < top_k:
                direct_papers.append(isa_p)
                seen[pid] = isa_p
        direct_papers.sort(key=lambda x: x.get("score", 0), reverse=True)
        logger.info("IS_A supplemented %d papers (direct < top_k)", len(direct_papers))

    papers_list = direct_papers

    if not papers_list:
        return "No relevant papers found for this query.", []

    # Step 4: Graph hint — context에 넣지 않고 system prompt에 1줄만
    paper_ids = [r["paper_id"] for r in papers_list]
    graph_hint = await _build_graph_hint(neo4j_client, paper_ids)

    # Build context — v18: chunk 텍스트 확장 (500→1000자)
    content_limit = 1000 if _b4_version == "v18" else 500
    context_parts = []
    papers = []
    for r in papers_list:
        content = r['content'][:content_limit]
        context_parts.append(
            f"[{r['paper_id']}] {r['title']} (Evidence: {r['evidence_level']})\n{content}"
        )
        papers.append({
            "paper_id": r["paper_id"], "title": r["title"],
            "evidence_level": r["evidence_level"], "score": r["score"],
        })

    context = "\n\n".join(context_parts)

    # Step 5: 답변 생성 프롬프트
    if _b4_version in ("v17", "v20"):
        # v17: 정량 데이터 추출 강조 프롬프트
        system_prompt = (
            "You are a spine surgery evidence synthesis system. "
            "Answer the clinical question based ONLY on the provided papers.\n\n"
            "CRITICAL INSTRUCTIONS:\n"
            "1. Extract and report ALL specific numbers: p-values, odds ratios, confidence intervals, "
            "percentages, means, sample sizes, NNT/NNH, effect sizes\n"
            "2. Cite the paper ID in brackets [paper_id] for EVERY claim\n"
            "3. For each study, state: design (RCT/meta-analysis/cohort), sample size, key quantitative findings\n"
            "4. Compare interventions using specific numerical differences, not vague terms like 'similar' or 'comparable'\n"
            "5. Organize by outcome domain with a summary table if possible\n"
            "6. If a paper reports complication rates, ALWAYS include the specific rates\n"
            "7. Acknowledge gaps in the evidence\n"
            "Do NOT use any knowledge outside the provided papers. "
            "Do NOT say 'not provided' if the data is in the papers — look carefully."
        )
    else:
        system_prompt = (
            "You are a spine surgery evidence synthesis system. "
            "Answer the clinical question based ONLY on the provided papers. For each claim:\n"
            "1. Cite the paper ID in brackets [paper_id]\n"
            "2. Mention the study design and evidence level\n"
            "3. Include specific numbers when available\n"
            "4. Organize by outcome domain\n"
            "5. Acknowledge gaps in the evidence\n"
            "Do NOT use any knowledge outside the provided papers."
        )

    if graph_hint:
        system_prompt += f"\n\nKnowledge graph context: {graph_hint}"

    # v9: GR 질문에만 Graph traversal 결과 추가 (system prompt에 요약)
    graph_traversal_summary = await _get_graph_traversal_summary(neo4j_client, question)
    if graph_traversal_summary:
        system_prompt += f"\n\nGraph traversal evidence:\n{graph_traversal_summary}"

    # v19: Sonnet 모델 사용
    if _b4_version == "v19":
        answer = await _call_llm(
            system_prompt=system_prompt,
            user_prompt=f"## Retrieved Papers\n\n{context}\n\n## Clinical Question\n\n{question}",
            max_tokens=4000,
            model_override="claude-sonnet-4-20250514",
        )
    else:
        answer = await _call_llm(
            system_prompt=system_prompt,
            user_prompt=f"## Retrieved Papers\n\n{context}\n\n## Clinical Question\n\n{question}",
            max_tokens=4000,
        )
    return answer, papers


async def _build_graph_hint(neo4j_client: Any, paper_ids: list[str]) -> str:
    """Build a 1-line graph hint for the system prompt (not context)."""
    if not paper_ids:
        return ""
    try:
        query = """
        UNWIND $pids AS pid
        MATCH (p:Paper {paper_id: pid})-[:INVESTIGATES]->(i:Intervention)
        OPTIONAL MATCH (i)-[:TREATS]->(path:Pathology)
        RETURN collect(DISTINCT i.name) AS interventions,
               collect(DISTINCT path.name) AS pathologies
        """
        rows = await neo4j_client.run_query(query, {"pids": paper_ids[:10]})
        if rows:
            ints = [i for i in (rows[0].get("interventions") or []) if i][:5]
            paths = [p for p in (rows[0].get("pathologies") or []) if p][:3]
            if ints and paths:
                return f"These papers study {', '.join(ints)} for treating {', '.join(paths)}."
            elif ints:
                return f"These papers study {', '.join(ints)}."
        return ""
    except Exception:
        return ""


async def run_all_baselines(
    neo4j_client: Any,
    questions: list[dict],
    baselines: list[str] = None,
    top_k: int = 10,
    max_questions: int = None,
) -> dict[str, list[GeneratedAnswer]]:
    """Run all baselines on all questions."""
    if baselines is None:
        baselines = ["B1", "B2", "B3", "B4"]

    if max_questions:
        questions = questions[:max_questions]

    results: dict[str, list[GeneratedAnswer]] = {b: [] for b in baselines}

    for qi, q in enumerate(questions):
        qid = q["id"]
        question = q["question"]
        logger.info("[%d/%d] %s: %s", qi + 1, len(questions), qid, question[:60])

        for baseline in baselines:
            import time
            t0 = time.time()

            try:
                if baseline == "B1":
                    answer, papers = await generate_answer_b1_keyword(neo4j_client, question, top_k)
                elif baseline == "B2":
                    answer, papers = await generate_answer_b2_vector(neo4j_client, question, top_k)
                elif baseline == "B3":
                    answer, papers = await generate_answer_b3_llm_direct(question)
                elif baseline == "B4":
                    answer, papers = await generate_answer_b4_graphrag(neo4j_client, question, top_k)
                else:
                    continue
            except Exception as e:
                logger.error("  %s failed: %s", baseline, e)
                answer, papers = f"Error: {e}", []

            elapsed = int((time.time() - t0) * 1000)
            evidence_levels = [p.get("evidence_level", "unknown") for p in papers]

            ga = GeneratedAnswer(
                query_id=qid,
                question=question,
                baseline=baseline,
                answer=answer,
                cited_papers=papers,
                num_papers_retrieved=len(papers),
                evidence_levels=evidence_levels,
                generation_time_ms=elapsed,
            )
            results[baseline].append(ga)
            logger.info("  %s: %d papers, %dms", baseline, len(papers), elapsed)

    return results


def save_answers(results: dict[str, list[GeneratedAnswer]], output_dir: Path = None):
    """Save generated answers to JSON files."""
    if output_dir is None:
        output_dir = EVAL_DIR / "results" / "answers"
    output_dir.mkdir(parents=True, exist_ok=True)

    for baseline, answers in results.items():
        data = [
            {
                "query_id": a.query_id,
                "question": a.question,
                "baseline": a.baseline,
                "answer": a.answer,
                "cited_papers": a.cited_papers,
                "num_papers_retrieved": a.num_papers_retrieved,
                "evidence_levels": a.evidence_levels,
                "generation_time_ms": a.generation_time_ms,
            }
            for a in answers
        ]
        path = output_dir / f"{baseline}.json"
        with open(path, "w") as f:
            json.dump(data, f, indent=2, ensure_ascii=False, default=str)
        logger.info("Saved %s: %d answers → %s", baseline, len(data), path)


