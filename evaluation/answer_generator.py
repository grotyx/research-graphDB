"""Answer generator for each baseline search mode.

For each question, runs the baseline search and generates an LLM answer
grounded in the retrieved papers. Output is used for LLM-as-Judge evaluation.

Usage:
    PYTHONPATH=./src:. python3 evaluation/answer_generator.py --baselines B1,B4 --questions 5
"""

import asyncio
import json
import logging
import os
from dataclasses import dataclass, field
from datetime import datetime
from pathlib import Path
from typing import Any, Optional

logger = logging.getLogger(__name__)
EVAL_DIR = Path(__file__).parent


@dataclass
class GeneratedAnswer:
    """Answer generated by a baseline system."""
    query_id: str
    question: str
    baseline: str
    answer: str
    cited_papers: list[dict] = field(default_factory=list)
    num_papers_retrieved: int = 0
    evidence_levels: list[str] = field(default_factory=list)
    generation_time_ms: int = 0


async def generate_answer_b1_keyword(
    neo4j_client: Any, question: str, top_k: int = 10
) -> tuple[str, list[dict]]:
    """B1: Keyword search → answer generation."""
    from baselines import KeywordSearch

    searcher = KeywordSearch(neo4j_client)
    results = await searcher.search(question, top_k=top_k)

    if not results:
        return "No relevant papers found for this query.", []

    context = _build_context(results)
    answer = await _generate_with_llm(question, context)
    papers = [{"paper_id": r.paper_id, "title": r.title, "evidence_level": r.evidence_level, "year": r.year} for r in results]
    return answer, papers


async def generate_answer_b2_vector(
    neo4j_client: Any, question: str, top_k: int = 10
) -> tuple[str, list[dict]]:
    """B2: Vector-only search → answer generation."""
    embedding = await _get_embedding(question)

    rows = await neo4j_client.vector_search_chunks(
        embedding=embedding, top_k=top_k * 3, min_score=0.3
    )

    # Dedup by paper
    seen = {}
    for r in rows:
        pid = r.get("paper_id", "")
        if pid and pid not in seen:
            seen[pid] = r

    papers_list = list(seen.values())[:top_k]
    if not papers_list:
        return "No relevant papers found for this query.", []

    # Build context from chunks
    context_parts = []
    papers = []
    for r in papers_list:
        title = r.get("paper_title", "Unknown")
        el = r.get("evidence_level", "unknown")
        content = r.get("content", "")[:500]
        pid = r.get("paper_id", "")
        context_parts.append(f"[{pid}] {title} (Evidence: {el})\n{content}")
        papers.append({"paper_id": pid, "title": title, "evidence_level": el})

    context = "\n\n".join(context_parts)
    answer = await _generate_with_llm(question, context)
    return answer, papers


async def generate_answer_b3_llm_direct(question: str) -> tuple[str, list[dict]]:
    """B3: LLM direct — no knowledge graph."""
    import anthropic

    client = anthropic.Anthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))
    response = client.messages.create(
        model="claude-haiku-4-5-20251001",
        max_tokens=2000,
        system=(
            "You are a spine surgery expert. Answer the clinical question based on "
            "your medical knowledge. For each point, cite specific studies by first "
            "author and year. Be specific about study designs and evidence levels. "
            "Organize your response by outcome domain."
        ),
        messages=[{"role": "user", "content": question}],
    )
    answer = response.content[0].text
    return answer, []  # No verifiable papers


async def generate_answer_b4_graphrag(
    neo4j_client: Any, question: str, top_k: int = 10
) -> tuple[str, list[dict]]:
    """B4: GraphRAG Pattern 2 -- Graph Filter -> Vector Rank.

    Pipeline:
        Step 1: LLM extracts entities (interventions, pathologies, outcomes)
        Step 2: Graph filter — find papers connected via STUDIES/INVESTIGATES
        Step 3: Broaden if too few papers found
        Step 4: Vector rank within filtered papers
        Step 5: PubMed fallback if still < 3 papers
        Step 6: Generate answer with evidence chain structure
    """
    # Step 1: Extract entities from question using Haiku
    entities = await _extract_entities_from_question(question)
    logger.info("B4 entities: %s", entities)

    # Step 2: Graph filter — find papers connected to extracted entities
    paper_ids = await _graph_filter_papers(neo4j_client, entities)
    logger.info("B4 graph filter: %d papers", len(paper_ids))

    # Step 3: If graph filter returns too few, broaden with keyword matching
    if len(paper_ids) < 5:
        paper_ids = await _broaden_search(neo4j_client, entities, paper_ids)
        logger.info("B4 after broadening: %d papers", len(paper_ids))

    # Step 4: Vector rank within filtered papers
    embedding = await _get_embedding(question)
    chunks = await _vector_search_within_papers(
        neo4j_client, embedding, paper_ids, top_k * 2
    )

    # Step 5: PubMed fallback if still < 3 papers
    supplementary_context: list[str] = []
    unique_pids = {c.get("paper_id", "") for c in chunks if c.get("paper_id")}
    if len(unique_pids) < 3:
        supplementary_context = await _pubmed_fallback(question)
        logger.info("B4 PubMed fallback: %d supplementary abstracts", len(supplementary_context))

    # Step 6: Dedup by paper, build context
    seen: dict[str, dict] = {}
    for c in chunks:
        pid = c.get("paper_id", "")
        if not pid:
            continue
        score = c.get("score", 0.0)
        if pid not in seen or score > seen[pid].get("score", 0):
            seen[pid] = {
                "paper_id": pid,
                "title": c.get("title", "Unknown"),
                "evidence_level": c.get("evidence_level", "unknown"),
                "content": c.get("content", "")[:500],
                "year": c.get("year"),
                "score": score,
            }

    papers_list = sorted(
        seen.values(), key=lambda x: x.get("score", 0), reverse=True
    )[:top_k]

    if not papers_list and not supplementary_context:
        return "No relevant papers found for this query.", []

    # Build context
    context_parts = []
    papers = []
    for r in papers_list:
        title = r["title"]
        el = r["evidence_level"]
        content = r["content"]
        pid = r["paper_id"]
        score = r["score"]
        year = r.get("year", "N/A")
        context_parts.append(
            f"[{pid}] {title} (Evidence: {el}, Year: {year}, Score: {score:.3f})\n{content}"
        )
        papers.append({
            "paper_id": pid, "title": title,
            "evidence_level": el, "score": score,
        })

    # Append PubMed supplementary context
    if supplementary_context:
        context_parts.append("\n--- Supplementary PubMed Context ---")
        context_parts.extend(supplementary_context)

    context = "\n\n".join(context_parts)

    # Step 7: Generate answer with evidence chain structure
    answer = await _generate_with_evidence_chain(question, context, entities)
    return answer, papers


async def run_all_baselines(
    neo4j_client: Any,
    questions: list[dict],
    baselines: list[str] = None,
    top_k: int = 10,
    max_questions: int = None,
) -> dict[str, list[GeneratedAnswer]]:
    """Run all baselines on all questions."""
    if baselines is None:
        baselines = ["B1", "B2", "B3", "B4"]

    if max_questions:
        questions = questions[:max_questions]

    results: dict[str, list[GeneratedAnswer]] = {b: [] for b in baselines}

    for qi, q in enumerate(questions):
        qid = q["id"]
        question = q["question"]
        logger.info("[%d/%d] %s: %s", qi + 1, len(questions), qid, question[:60])

        for baseline in baselines:
            import time
            t0 = time.time()

            try:
                if baseline == "B1":
                    answer, papers = await generate_answer_b1_keyword(neo4j_client, question, top_k)
                elif baseline == "B2":
                    answer, papers = await generate_answer_b2_vector(neo4j_client, question, top_k)
                elif baseline == "B3":
                    answer, papers = await generate_answer_b3_llm_direct(question)
                elif baseline == "B4":
                    answer, papers = await generate_answer_b4_graphrag(neo4j_client, question, top_k)
                else:
                    continue
            except Exception as e:
                logger.error("  %s failed: %s", baseline, e)
                answer, papers = f"Error: {e}", []

            elapsed = int((time.time() - t0) * 1000)
            evidence_levels = [p.get("evidence_level", "unknown") for p in papers]

            ga = GeneratedAnswer(
                query_id=qid,
                question=question,
                baseline=baseline,
                answer=answer,
                cited_papers=papers,
                num_papers_retrieved=len(papers),
                evidence_levels=evidence_levels,
                generation_time_ms=elapsed,
            )
            results[baseline].append(ga)
            logger.info("  %s: %d papers, %dms", baseline, len(papers), elapsed)

    return results


def save_answers(results: dict[str, list[GeneratedAnswer]], output_dir: Path = None):
    """Save generated answers to JSON files."""
    if output_dir is None:
        output_dir = EVAL_DIR / "results" / "answers"
    output_dir.mkdir(parents=True, exist_ok=True)

    for baseline, answers in results.items():
        data = [
            {
                "query_id": a.query_id,
                "question": a.question,
                "baseline": a.baseline,
                "answer": a.answer,
                "cited_papers": a.cited_papers,
                "num_papers_retrieved": a.num_papers_retrieved,
                "evidence_levels": a.evidence_levels,
                "generation_time_ms": a.generation_time_ms,
            }
            for a in answers
        ]
        path = output_dir / f"{baseline}.json"
        with open(path, "w") as f:
            json.dump(data, f, indent=2, ensure_ascii=False, default=str)
        logger.info("Saved %s: %d answers → %s", baseline, len(data), path)


